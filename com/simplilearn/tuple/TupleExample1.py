print("***************START: Tuple Representation Example ***************")

countryName=("INDIA","USA","UK","SRI LANKA","CHINA","ITLY")
print(type(countryName)) # <class 'tuple'>
print(countryName)
print("-----------------------------------")

myName=("Aditya")
print(type(myName)) # <class 'str'>
print(myName)

print("-----------------------------------")
myName=("Aditya",)
print(type(myName)) # <class 'tuple'>
print(myName)

print("-----------------------------------")
cityTuple="Bangalore","Pune","Delhi","Hyderabad"
print(type(cityTuple)) # <class 'tuple'>
print(cityTuple)

print("-----------------------------------")
mycity="Bangalore",
print(type(mycity)) # <class 'tuple'>
print(mycity)

print("***************END: Tuple Representation Example ***************")